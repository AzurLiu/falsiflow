#!/usr/bin/env python3
"""Render and check the vendor-return inbox for real NHI-PEDOT H-A measurements."""

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any

from merge_nhi_pedot_h_a_raw_measurements import RAW_FIELDS
from render_nhi_pedot_h_a_bundle_entry_sheet import SHEET_FIELDS


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_RETURN_DIR = ROOT / "data" / "nhi_pedot_h_a_vendor_return_inbox"
DEFAULT_EXPECTED_CUSTODY = ROOT / "data" / "nhi_pedot_h_a_chain_of_custody.csv"
DEFAULT_CHECKLIST_CSV = ROOT / "data" / "nhi_pedot_h_a_vendor_return_checklist.csv"
DEFAULT_JSON = ROOT / "data" / "nhi_pedot_h_a_vendor_return_intake.json"
DEFAULT_REPORT = ROOT / "reports" / "nhi_pedot_h_a_vendor_return_intake.md"

RETURN_RAW = "completed_raw_measurements.csv"
RETURN_BUNDLE_SHEET = "completed_bundle_entry_sheet.csv"
RETURN_CUSTODY = "completed_chain_of_custody.csv"
RETURN_DEVIATION_LOG = "deviation_log.md"
RETURN_EXPORT_DIR = "external_lab_exports"

CHECKLIST_FIELDS = [
    "item_id",
    "expected_path",
    "required",
    "exists",
    "status",
    "acceptance",
]

NON_EVIDENCE_BOUNDARY = (
    "This intake report only checks whether returned files are ready for merge/QC. "
    "Returned files are not material evidence until real rows pass merge, intake QC, H-A interpretation, and the final claim audit."
)


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def load_csv(path: Path) -> tuple[list[str], list[dict[str, str]]]:
    if not path.exists():
        return [], []
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        return list(reader.fieldnames or []), list(reader)


def write_csv(path: Path, fields: list[str], rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def write_readme(return_dir: Path) -> Path:
    path = return_dir / "README.md"
    return_dir.mkdir(parents=True, exist_ok=True)
    text = "\n".join([
        "# NHI-PEDOT H-A Vendor Return Inbox",
        "",
        "Place real vendor/cooperator return files here. Do not place synthetic fixtures here.",
        "",
        "Expected files:",
        "",
        f"- `{RETURN_RAW}`: completed long-form raw measurement CSV using the LIMINA schema.",
        f"- `{RETURN_BUNDLE_SHEET}`: optional compact one-row-per-source-file bundle sheet generated by `render_nhi_pedot_h_a_vendor_bundle_entry_sheet.py` and returned by the provider.",
        f"- `{RETURN_CUSTODY}`: completed sample/coupon chain-of-custody CSV.",
        f"- `{RETURN_EXPORT_DIR}/`: original instrument exports and images referenced by `source_file`.",
        f"- `{RETURN_DEVIATION_LOG}`: deviations from timing, instruments, sample handling, medium, or recipe.",
        "",
        "After files are present, run:",
        "",
        "```bash",
        "python3 scripts/render_nhi_pedot_h_a_vendor_return_intake.py",
        "python3 scripts/render_nhi_pedot_h_a_vendor_bundle_entry_sheet.py",
        f"python3 scripts/apply_nhi_pedot_h_a_vendor_bundle_entry_return.py --sheet data/nhi_pedot_h_a_vendor_return_inbox/{RETURN_BUNDLE_SHEET}",
        "python3 scripts/import_nhi_pedot_h_a_source_values.py",
        "python3 scripts/merge_nhi_pedot_h_a_raw_measurements.py",
        "python3 scripts/run_limina_iteration.py",
        "```",
        "",
    ])
    if (
        not path.exists()
        or "import_nhi_pedot_h_a_source_values.py" not in path.read_text(encoding="utf-8")
        or "render_nhi_pedot_h_a_vendor_bundle_entry_sheet.py" not in path.read_text(encoding="utf-8")
    ):
        path.write_text(text, encoding="utf-8")
    return path


def source_file_candidates(return_dir: Path, export_dir: Path, source_file: str) -> list[Path]:
    source = source_file.strip()
    if not source:
        return []
    path = Path(source)
    if path.is_absolute():
        return [path]
    return [
        return_dir / source,
        export_dir / source,
        ROOT / source,
    ]


def count_export_files(export_dir: Path) -> int:
    if not export_dir.exists() or not export_dir.is_dir():
        return 0
    return sum(1 for path in export_dir.rglob("*") if path.is_file())


def analyze_raw(raw_path: Path, return_dir: Path, export_dir: Path) -> dict[str, Any]:
    fields, rows = load_csv(raw_path)
    missing_fields = [field for field in RAW_FIELDS if field not in fields]
    value_rows = [row for row in rows if row.get("value", "").strip()]
    missing_source_file = 0
    missing_source_refs = []
    for index, row in enumerate(value_rows, start=1):
        source_file = row.get("source_file", "").strip()
        if not source_file:
            missing_source_file += 1
            missing_source_refs.append({
                "raw_value_row": index,
                "run_id": row.get("run_id", ""),
                "target_field": row.get("target_field", ""),
                "issue": "source_file is blank",
            })
            continue
        if not any(candidate.exists() for candidate in source_file_candidates(return_dir, export_dir, source_file)):
            missing_source_file += 1
            missing_source_refs.append({
                "raw_value_row": index,
                "run_id": row.get("run_id", ""),
                "target_field": row.get("target_field", ""),
                "source_file": source_file,
                "issue": "source_file does not resolve in return inbox, export dir, or workspace root",
            })
    return {
        "path": rel(raw_path),
        "exists": raw_path.exists(),
        "schema_ok": raw_path.exists() and not missing_fields,
        "fields": fields,
        "missing_fields": missing_fields,
        "row_count": len(rows),
        "value_row_count": len(value_rows),
        "missing_or_unresolved_source_file_count": missing_source_file,
        "missing_or_unresolved_source_file_examples": missing_source_refs[:20],
    }


def analyze_bundle_entry_sheet(sheet_path: Path, return_dir: Path, export_dir: Path) -> dict[str, Any]:
    fields, rows = load_csv(sheet_path)
    missing_fields = [field for field in SHEET_FIELDS if field not in fields]
    apply_rows = [row for row in rows if row.get("apply", "").strip().lower() in {"yes", "true", "1", "y"}]
    ready_rows = [row for row in apply_rows if row.get("entry_status", "") == "ready_to_apply"]
    blocked_rows = [row for row in apply_rows if row.get("entry_status", "") == "blocked"]
    missing_source_file = 0
    missing_source_refs = []
    for index, row in enumerate(apply_rows, start=1):
        source_file = row.get("source_file", "").strip()
        if not source_file:
            missing_source_file += 1
            missing_source_refs.append({
                "bundle_row": index,
                "bundle_id": row.get("bundle_id", ""),
                "issue": "source_file is blank",
            })
            continue
        if not any(candidate.exists() for candidate in source_file_candidates(return_dir, export_dir, source_file)):
            missing_source_file += 1
            missing_source_refs.append({
                "bundle_row": index,
                "bundle_id": row.get("bundle_id", ""),
                "source_file": source_file,
                "issue": "source_file does not resolve in return inbox, export dir, or workspace root",
            })
    return {
        "path": rel(sheet_path),
        "exists": sheet_path.exists(),
        "schema_ok": sheet_path.exists() and not missing_fields,
        "fields": fields,
        "missing_fields": missing_fields,
        "row_count": len(rows),
        "apply_row_count": len(apply_rows),
        "ready_to_apply_rows": len(ready_rows),
        "blocked_apply_rows": len(blocked_rows),
        "missing_or_unresolved_source_file_count": missing_source_file,
        "missing_or_unresolved_source_file_examples": missing_source_refs[:20],
    }


def analyze_custody(custody_path: Path, expected_custody: Path) -> dict[str, Any]:
    fields, rows = load_csv(custody_path)
    expected_fields, _ = load_csv(expected_custody)
    missing_fields = [field for field in expected_fields if field not in fields]
    return {
        "path": rel(custody_path),
        "exists": custody_path.exists(),
        "schema_ok": custody_path.exists() and not missing_fields,
        "missing_fields": missing_fields,
        "row_count": len(rows),
        "pending_rows": sum(1 for row in rows if row.get("transfer_status", "") == "pending_real_transfer"),
    }


def checklist_rows(
    return_dir: Path,
    raw: dict[str, Any],
    bundle_entry: dict[str, Any],
    custody: dict[str, Any],
    export_count: int,
) -> list[dict[str, Any]]:
    items = [
        {
            "item_id": "completed_raw_measurements",
            "expected_path": rel(return_dir / RETURN_RAW),
            "required": "true",
            "exists": str(raw["exists"]).lower(),
            "status": "ok" if raw["schema_ok"] and raw["value_row_count"] > 0 else "missing_or_incomplete",
            "acceptance": "CSV has RAW_FIELDS schema, real value rows, provenance fields, and source_file references.",
        },
        {
            "item_id": "completed_bundle_entry_sheet",
            "expected_path": rel(return_dir / RETURN_BUNDLE_SHEET),
            "required": "false",
            "exists": str(bundle_entry["exists"]).lower(),
            "status": "ok" if bundle_entry["ready_to_apply_rows"] > 0 and bundle_entry["missing_or_unresolved_source_file_count"] == 0 else "missing_or_incomplete",
            "acceptance": "Optional compact CSV has bundle-entry schema, ready apply=yes rows, and source_file references.",
        },
        {
            "item_id": "completed_chain_of_custody",
            "expected_path": rel(return_dir / RETURN_CUSTODY),
            "required": "true",
            "exists": str(custody["exists"]).lower(),
            "status": "ok" if custody["schema_ok"] and custody["row_count"] > 0 else "missing_or_incomplete",
            "acceptance": "CSV uses the chain-of-custody schema with transfer fields filled for returned samples.",
        },
        {
            "item_id": "instrument_exports",
            "expected_path": rel(return_dir / RETURN_EXPORT_DIR),
            "required": "true",
            "exists": str((return_dir / RETURN_EXPORT_DIR).exists()).lower(),
            "status": "ok" if export_count > 0 else "missing_or_empty",
            "acceptance": "Directory contains original instrument exports/images referenced by source_file.",
        },
        {
            "item_id": "deviation_log",
            "expected_path": rel(return_dir / RETURN_DEVIATION_LOG),
            "required": "true",
            "exists": str((return_dir / RETURN_DEVIATION_LOG).exists()).lower(),
            "status": "ok" if (return_dir / RETURN_DEVIATION_LOG).exists() else "missing",
            "acceptance": "Markdown or text log states no deviations or lists every protocol/sample/instrument deviation.",
        },
    ]
    return items


def build_intake(return_dir: Path, expected_custody: Path) -> dict[str, Any]:
    readme = write_readme(return_dir)
    raw_path = return_dir / RETURN_RAW
    bundle_entry_path = return_dir / RETURN_BUNDLE_SHEET
    custody_path = return_dir / RETURN_CUSTODY
    export_dir = return_dir / RETURN_EXPORT_DIR
    raw = analyze_raw(raw_path, return_dir, export_dir)
    bundle_entry = analyze_bundle_entry_sheet(bundle_entry_path, return_dir, export_dir)
    custody = analyze_custody(custody_path, expected_custody)
    export_count = count_export_files(export_dir)
    checklist = checklist_rows(return_dir, raw, bundle_entry, custody, export_count)

    files_present = (
        raw["exists"]
        or bundle_entry["apply_row_count"] > 0
        or custody["exists"]
        or export_count > 0
        or (return_dir / RETURN_DEVIATION_LOG).exists()
    )
    required_ok = all(row["status"] == "ok" for row in checklist if row["required"] == "true")
    source_ok = raw["missing_or_unresolved_source_file_count"] == 0
    bundle_ready = (
        bundle_entry["schema_ok"]
        and bundle_entry["ready_to_apply_rows"] > 0
        and bundle_entry["blocked_apply_rows"] == 0
        and bundle_entry["missing_or_unresolved_source_file_count"] == 0
    )
    if required_ok and source_ok:
        status = "vendor_return_ready_for_merge"
    elif bundle_ready:
        status = "vendor_return_ready_for_bundle_entry_apply"
    elif files_present:
        status = "vendor_return_needs_attention"
    else:
        status = "awaiting_vendor_return_files"

    return {
        "status": status,
        "purpose": "Check whether real vendor/cooperator H-A return files are ready for merge and QC.",
        "return_dir": rel(return_dir),
        "readme": rel(readme),
        "raw_measurements": raw,
        "bundle_entry_sheet": bundle_entry,
        "chain_of_custody": custody,
        "instrument_export_dir": rel(export_dir),
        "instrument_export_file_count": export_count,
        "deviation_log": {
            "path": rel(return_dir / RETURN_DEVIATION_LOG),
            "exists": (return_dir / RETURN_DEVIATION_LOG).exists(),
        },
        "checklist_rows": checklist,
        "post_return_commands": [
            "python3 scripts/render_nhi_pedot_h_a_vendor_bundle_entry_sheet.py",
            f"python3 scripts/apply_nhi_pedot_h_a_vendor_bundle_entry_return.py --sheet {rel(bundle_entry_path)}",
            "python3 scripts/import_nhi_pedot_h_a_source_values.py",
            "python3 scripts/merge_nhi_pedot_h_a_raw_measurements.py",
            "python3 scripts/qc_nhi_pedot_h_a_intake.py --runs data/nhi_pedot_h_a_runs_active.csv --strict-exit",
            "python3 scripts/interpret_nhi_pedot_h_a_sentinel.py --runs data/nhi_pedot_h_a_runs_active.csv",
            "python3 scripts/run_limina_iteration.py",
        ],
        "non_evidence_boundary": NON_EVIDENCE_BOUNDARY,
    }


def render_report(result: dict[str, Any]) -> str:
    raw = result["raw_measurements"]
    bundle_entry = result["bundle_entry_sheet"]
    custody = result["chain_of_custody"]
    lines = [
        "# NHI-PEDOT H-A Vendor Return Intake",
        "",
        "This report checks the return inbox for real H-A measurement files. It is not measured evidence.",
        "",
        f"**Status:** `{result['status']}`",
        f"**Return directory:** `{result['return_dir']}`",
        f"**Inbox README:** `{result['readme']}`",
        "",
        "## File Checklist",
        "",
        "| Item | Required | Exists | Status | Path |",
        "| --- | --- | --- | --- | --- |",
    ]
    for row in result["checklist_rows"]:
        lines.append(
            f"| `{row['item_id']}` | `{row['required']}` | `{row['exists']}` | "
            f"`{row['status']}` | `{row['expected_path']}` |"
        )

    lines.extend([
        "",
        "## Raw Measurement CSV",
        "",
        f"- Path: `{raw['path']}`",
        f"- Exists: `{raw['exists']}`",
        f"- Schema OK: `{raw['schema_ok']}`",
        f"- Rows: {raw['row_count']}",
        f"- Rows with values: {raw['value_row_count']}",
        f"- Missing/unresolved source_file references: {raw['missing_or_unresolved_source_file_count']}",
    ])
    if raw["missing_fields"]:
        lines.append(f"- Missing fields: {', '.join(f'`{field}`' for field in raw['missing_fields'])}")

    lines.extend([
        "",
        "## Bundle Entry Sheet",
        "",
        f"- Path: `{bundle_entry['path']}`",
        f"- Exists: `{bundle_entry['exists']}`",
        f"- Schema OK: `{bundle_entry['schema_ok']}`",
        f"- Rows: {bundle_entry['row_count']}",
        f"- Apply rows: {bundle_entry['apply_row_count']}",
        f"- Ready to apply: {bundle_entry['ready_to_apply_rows']}",
        f"- Blocked apply rows: {bundle_entry['blocked_apply_rows']}",
        f"- Missing/unresolved source_file references: {bundle_entry['missing_or_unresolved_source_file_count']}",
    ])
    if bundle_entry["missing_fields"]:
        lines.append(f"- Missing fields: {', '.join(f'`{field}`' for field in bundle_entry['missing_fields'])}")

    lines.extend([
        "",
        "## Chain Of Custody",
        "",
        f"- Path: `{custody['path']}`",
        f"- Exists: `{custody['exists']}`",
        f"- Schema OK: `{custody['schema_ok']}`",
        f"- Rows: {custody['row_count']}",
        f"- Pending transfer rows: {custody['pending_rows']}",
        "",
        "## Instrument Exports",
        "",
        f"- Directory: `{result['instrument_export_dir']}`",
        f"- File count: {result['instrument_export_file_count']}",
        "",
        "## Post-Return Commands",
        "",
        "```bash",
        *result["post_return_commands"],
        "```",
        "",
        "## Boundary",
        "",
        result["non_evidence_boundary"],
        "",
    ])
    return "\n".join(lines)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Render NHI-PEDOT H-A vendor return intake.")
    parser.add_argument("--return-dir", type=Path, default=DEFAULT_RETURN_DIR)
    parser.add_argument("--expected-custody", type=Path, default=DEFAULT_EXPECTED_CUSTODY)
    parser.add_argument("--checklist-csv", type=Path, default=DEFAULT_CHECKLIST_CSV)
    parser.add_argument("--json-out", type=Path, default=DEFAULT_JSON)
    parser.add_argument("--report", type=Path, default=DEFAULT_REPORT)
    return parser


def main() -> int:
    args = build_parser().parse_args()
    result = build_intake(args.return_dir, args.expected_custody)
    write_csv(args.checklist_csv, CHECKLIST_FIELDS, result["checklist_rows"])
    args.json_out.parent.mkdir(parents=True, exist_ok=True)
    args.json_out.write_text(json.dumps(result, indent=2, sort_keys=True), encoding="utf-8")
    args.report.parent.mkdir(parents=True, exist_ok=True)
    args.report.write_text(render_report(result), encoding="utf-8")
    print(f"H-A vendor return intake: {result['status']}")
    print(f"Wrote {args.checklist_csv}")
    print(f"Wrote {args.json_out}")
    print(f"Wrote {args.report}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
