DEFAULT_EXPECTED_CUSTODY = ROOT / "data" / "zrc_nd_phase_a_chain_of_custody.csv"
DEFAULT_CHECKLIST_CSV = ROOT / "data" / "zrc_nd_phase_a_vendor_return_checklist.csv"
DEFAULT_BUNDLE_ENTRY_APPLY = ROOT / "data" / "zrc_nd_phase_a_vendor_bundle_entry_apply.json"
DEFAULT_JSON = ROOT / "data" / "zrc_nd_phase_a_vendor_return_intake.json"
